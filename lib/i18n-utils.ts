export function translateMessage(message: string, language: AppLanguage = getStoredLanguage()) {
  if (language !== 'ar') return message;

  const allTranslations = translations.ar as Record<string, string>;
  if (allTranslations[message]) return allTranslations[message];

  return messageTranslations[message] || message;
}
